#include<iostream>
using namespace std;

int main()
{
    int i;
    char ch;
    float f;
    double d;
    cout<<"Size of char  : "<<sizeof(ch)<<" byte"<<"\n";
    cout<<"Size of int   : "<<sizeof(i)<<" bytes" <<"\n";
    cout<<"Size of float : "<<sizeof(f)<<" bytes"<<"\n";
    cout<<"Size of double: "<<sizeof(d)<<" bytes";
}
